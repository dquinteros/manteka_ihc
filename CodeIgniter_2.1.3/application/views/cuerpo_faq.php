<fieldset >
	<legend>Preguntas Frecuentes</legend>
	<div class="faqs">
	<h4>1. ¿Qué es MANTEKA?</h4>
		<div>
			<p>MANTEKA es una sigla referente a "Mantenedor de Kilos de Alumnos", cuya función principal es coordinar una gran cantidad de estudiantes que cursan la asignatura de Comunicación Efectiva, impartida en la Universidad de Santiago de Chile.</p>
		</div>
	<h4>2. ¿Los correos enviados solamente son los provenientes desde MANTEKA?</h4>
		<div>
			<p>Si, todo correo electrónico enviado desde una aplicación que no sea MANTEKA no quedará registrado, MANTEKA solo registrará aquellos mensajes enviados desde la aplicación, con el fin de mantener un orden en la bandeja de correos enviados.</p>
		</div>
	<h4>3. ¿Es posible enviar un correo electrónico a distintos destinatarios?</h4>
		<div>
			<p>Si, el usuario tiene la posibilidad de enviar un mensaje a los destinatarios que desee, ya sea mediante el uso de los filtros de información disponible o de manera directa, también tendrá la opción de crear grupos permanentes de contactos para agilizar la tarea de selección de destinatarios, para más información puede consultar el manual de usuario de la aplicación. </p>
		</div>
	<h4>4. ¿Qué son los borradores en la sección de Correos?</h4>
		<div>
			<p>Los borradores son aquellos mensajes que el usuario no envió, y por lo tanto quedarán guardados para que el usuario los pueda enviar cuando desee.</p>
		</div>
	<h4>5. ¿Por qué no puedo editar algunos datos ingresados?</h4>
		<div>
			<p>Efectivamente no es posible editar ciertos datos dentro de MANTEKA, debido a que la aplicación los considera vitales para el buen funcionamiento, pero el usuario en caso de comenter algun error, este puede eliminar a un profesor, alumnos, ayudante, etc., cuya información haya sido ingresada de forma incorrecta y no pueda ser editada, y reingresar de manera correcta los datos.</p>
		</div>
	<h4>6. ¿Por qué es necesario indicar los implementos de una sala?</h4>
		<div>
			<p>Es de suma importancia, ya que al conocer los elementos o implementos que una sala presenta, el profesor podrá tener conocimiento y la certeza de lo que necesita para impartir su clase, ya sea un proyector, un computador, una pizarra, etc.. Algunos profesores impartirán sus clases haciendo uso de diapositivas o presentaciones en MS Power Point, por lo que es necesario tener una sala con los implementos adecuados para la realización de la clase.</p>
		</div>
	<h4>7. ¿Qué es un filtro?</h4>
		<div>
			<p>Un filtro es una forma de seleccionar ciertas características de personas registradas en MANTEKA que el usuario puede seleccionar de tal forma de poder buscar cierta(s) persona(s) que cumplan con las características determinadas en el filtro.</p>
		</div>
	<h4>8. ¿Qué es una plantilla?</h4>
		<div>
			<p>Una plantilla es el texto modelo de un correo electrónico prediseñado y cuya finalidad es reutilizarlo para enviar correos a futuro sin tener que reescribir éste desde el principio. El usuario puede crear tantas plantillas como desee, de esta manera agilizará su trabajo</p>
		</div>
	<h4>9. ¿Qué es un grupo de contactos?</h4>
		<div>
			<p>Un grupo de contactos es un filtro que ha sido almacenado de tal forma de poder utilizarlo a futuro, la aplicación de este filtro genera un listado de contactos que cumplen con las características especificadas por el usuario en el pasado. Para mayor información puede descargar el manual de usuario de MANTEKA en la sección de Correos.</p>
		</div>
	<h4>10. ¿Qué es una sección?</h4>
		<div>
			<p>Una sección es el conjunto de estudiantes que conforman un curso de la asignatura Comunicación Efectiva, cada curso tiene asignado un moódulo temático a cargo de un profesor el cual impartirá los contenidos correspondientes.</p>
		</div>
	<h4>11. ¿Qué es un módulo?</h4>
		<div>
			<p>Un módulo corresponde a la unidad temática que el profesor impartirá durante las clases de la sección a la cual esta asignado.</p>
		</div>
	<h4>12. ¿En qué consisten las sesiones?</h4>
		<div>
			<p>Las sesiones son los distintos conceptos y contenidos que el profesor abordará en la realización del módulo temático, por ejemplo, el modulo temático Comunicación no verbal presentará sesiones como: ¿Qué es la comunicación?.</p>
		</div>
	<h4>13. ¿Qué es una asignación?</h4>
		<div>
			<p>Es la tarea de relacionar una sección con el modulo temático que será impartido, el profesor a cargo de la sección, la sala y horario de realización, en otras palabras es la construcción de la planificación de la asignatura.</p>
		</div>
	<h4>14. ¿Es posible enviar correos a personas externas a MANTEKA?</h4>
		<div>
			<p>No, MANTEKA solamente permite el envío de correos a personas registradas en el sistema, es decir, profesores, alumnos, coordinadores y ayudantes de la asignatura Comunicación Efectiva.</p>
		</div>
	<h4>15. ¿Existe la posibilidad de ingresar más de un alumno a la vez al sistema?</h4>
		<div>
			<p>Si, para agilizar el ingreso de nuevos estudiantes de manera rápida y segura existe la carga masiva de datos, la cual se realiza en la seccion de alumnos, donde solamente es necesario importar un archivo con los datos a ingresar. Para saber más sobre la carga masiva puede consultar al manual de usuario.</p>
		</div>	
	<h4>16. ¿Puedo ver los mails que he eliminado?</h4>
		<div>
			<p>Si, en la sección de correos existe la opción de "Log de eliminados" donde se encuentran aquellos correos que han sido eliminados en MANTEKA, el usuario podrá consultar por correos electronicos enviados, recibidos y borradores que hayan sido eliminados.</p>
		</div>	
	<h4>17. ¿Qué tipo de reportes puedo obtener de MANTEKA?</h4>
		<div>
			<p>Existen dos tipos de reportes, el primero trata sobre información relacionada con los alumnos de una sección en particular, asistencias, notas, registro de entradas, etc. Son los reportes de usuario. El segundo trata sobre datos relacionados con el sistema MANTEKA, vale decir, datos personales de estudiantes e información de secciones.</p>
		</div>	
	</div>

